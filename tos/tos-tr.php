<h3>Reşit Olmayanlar</h3>
<p>
Hizmetlerimiz 13 yaş ve üzeri kullanıcılar içindir ve bilerek 13 yaşın altındaki çocuklardan kişisel bilgi toplamayız. 13 yaşın altında bir çocuğun ebeveyni veya vasisi iseniz ve çocuğunuzun bize kişisel bilgilerini açıkladığını düşünüyorsanız, lütfen bizimle iletişime geçin. AEA sakinleri için, kişisel bilgilerin işlenmesinin rızaya dayalı olduğu durumlarda, DISBOARD bilerek bu işlemi geçerli veri koruma kanunu tarafından belirlenen rıza yaşının altındaki kullanıcılar için gerçekleştirmeyecektir. Bu tür kullanıcılarla bu işlemi yaptığımızı öğrenirsek, bu işlemi durduracak ve ilgili bilgileri kayıtlarımızdan kaldırmak için makul önlemler alacağız.
</p>

